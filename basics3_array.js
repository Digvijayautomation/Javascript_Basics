let marks=[1,2,3,4,5,6]
console.log(marks) // will print all array

console.log(marks[2]) // will print value at index 2

marks[2]=10 // will update index 2 value with 10
console.log(marks) 


marks.push(56) // will append element in the end of the array
marks.unshift(56) // will append element in the begining of the array
console.log(marks) 

marks.pop() // will delete element in the end of the array
console.log(marks) 


